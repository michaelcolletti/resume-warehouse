## Project Sonify
**Description:** Sonify all the things. This project focuses on converting various data inputs into sound, making information more accessible and engaging through auditory means.
**References:**
- [Sonification Handbook](https://sonification.de/handbook/)
- [Data Sonification Archive](https://www.sonification.com/)

## Project DomainLookUp
**Description:** Utility for Domains. This test project provides a comprehensive CLI and GUI tool for looking up domain information, helping users to quickly find and analyze domain data. The repo for this is [lookup-domain](https://github.com/michaelcolletti/lookup-domain).

**References:**
- [ICANN WHOIS](https://whois.icann.org/en)
- [DomainTools](https://github.com/michaelcolletti/lookup-domain)

## Project AllMyAllergies.com
**Description:** Create your very own static website based on your food sensitivities and deploy on our platform. This project aims to help individuals manage their food allergies by providing a personalized and easy-to-use website creation tool.
**References:**
- [Food Allergy Research & Education (FARE)](https://www.foodallergy.org/)
- [AllergyHome](https://www.allergyhome.org/)
